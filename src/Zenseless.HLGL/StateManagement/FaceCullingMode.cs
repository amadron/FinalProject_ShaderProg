namespace Zenseless.HLGL
{
	/// <summary>
	/// Mode for culling of face sides
	/// </summary>
	public enum FaceCullingMode
	{
		/// <summary>
		/// No side will be culled
		/// </summary>
		NONE,
		/// <summary>
		/// The front side will be culled
		/// </summary>
		FRONT_SIDE,
		/// <summary>
		/// The back side will be culled
		/// </summary>
		BACK_SIDE
	}
}
